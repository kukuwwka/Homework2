public class AgeOfUniverse {
    public static void main(String[] args) {
        final long ageOfOurUniverse = 13000000000L;
        final int ageOfHuman = 100000;
        final long howOldUniverseThanHuman = ageOfOurUniverse / ageOfHuman;
        System.out.println(howOldUniverseThanHuman);
    }
}
